// Package main implements staticlint — project multichecker.
//
// Запуск:
//
//	go run ./cmd/staticlint ./...
//
// или
//
//	go build -o staticlint ./cmd/staticlint
//	./staticlint ./...
//
// Состав multichecker’а:
//
//	 asmdecl      — сообщает о несоответствиях между кодом Go и ассемблерными файлами.
//	 assign       — обнаруживает бесполезные присваивания (запись в переменную, которая не используется).
//	 atomic       — проверяет распространенные ошибки неправильного использования пакета sync/atomic.
//	 bools        — ищет распространенные ошибки в логических выражениях (например, x || true).
//	 buildtag     — проверяет правильность формирования тегов сборки (build tags).
//	 cgocall      — обнаруживает нарушения правил передачи указателей в cgo.
//	 composite    — проверяет использование неключевых (unkeyed) составных литералов (например, struct{10} вместо struct{Field: 10}).
//	 copylock     — обнаруживает передачу блокировок (locks) по значению (что приводит к их копированию).
//	 defers       — проверяет распространенные ошибки при использовании defer (например, defer внутри цикла).
//	 directive    — проверяет правильность синтаксиса директив компилятора (//go:...).
//	 errorsas     — проверяет, что второй аргумент errors.As является указателем на ошибку.
//	 fieldalignment — обнаруживает структуры, которые можно переупорядочить для уменьшения расхода памяти.
//	 framepointer — сообщает об инструкциях ассемблера, которые затирают указатель фрейма (frame pointer).
//	 httpresponse — проверяет ошибки при обработке HTTP-ответов (например, отсутствие закрытия body).
//	 ifaceassert  — обнаруживает невозможные утверждения интерфейсов (type assertions).
//	 loopclosure  — проверяет захват переменных цикла внутри вложенных функций (распространенная ошибка в циклах).
//	 lostcancel   — обнаруживает контексты (context), функция отмены (cancel) которых никогда не вызывается.
//	 nilfunc      — проверяет сравнение функций с nil (что всегда ложно) и бесполезные проверки.
//	 printf       — проверяет соответствие форматных строк аргументам в функциях типа Printf.
//	 reflectvaluecompare — предупреждает о случайном сравнении reflect.Value с помощью == (нужно использовать .Interface()).
//	 shadow       — проверяет затенение переменных (переменная во внутреннем блоке скрывает переменную внешнего блока).
//	 shift        — проверяет подозрительные битовые сдвиги (например, сдвиг на число больше разрядности типа).
//	 sigchanyzer  — обнаруживает неправильное использование каналов с os/signal.Notify (буфер должен быть > 0).
//	 slog         — проверяет аргументы функций структурированного логирования log/slog (непарные ключи-значения).
//	 sortslice    — проверяет вызовы sort.Slice, где функция сравнения не соответствует сортируемому слайсу.
//	 stdmethods   — проверяет сигнатуры методов, требуемых стандартными интерфейсами (например, WriteByte).
//	 stringintconv — предупреждает о преобразовании string(x), где x — целое число (не руна).
//	 structtag    — проверяет правильность синтаксиса тегов структур (struct tags), например json:"...".
//	 testinggoroutine — предупреждает о вызовах t.Fatal/t.Skip и др. из горутин, отличных от той, что запустила тест.
//	 tests        — проверяет распространенные ошибки в тестах и примерах (Test*, Example*).
//	 timeformat   — проверяет правильность строк формата времени (layout) для time.Parse и time.Format.
//	 unmarshal    — обнаруживает передачу неуказателей или значений без полей в json.Unmarshal и подобные функции.
//	 unreachable  — находит недостижимый код (unreachable code).
//	 unsafeptr    — проверяет неправильное использование unsafe.Pointer (например, конвертация uintptr обратно в Pointer).
//	 unusedresult — проверяет неиспользуемые результаты вызовов функций (для функций, побочные эффекты которых важны).
//	 unusedwrite  — обнаруживает запись в поля структуры или массивы, которые никогда не читаются.
//
//	2. Staticcheck (класс SA*):
//	   Все анализаторы из staticcheck.Analyzers (SA1000..), проверяют ошибки, баги и perf issues.
//
//	3. Staticcheck (прочие классы):
//	   simple (S*)      — упрощения кода.
//	   stylecheck (ST*) — стиль и соглашения именования.
//	   unused (U1000)   — неиспользуемый код.
//
//	4. Сторонние публичные анализаторы:
//	   exhaustive — проверяет исчерпываемость switch по enum-подобным значениям.
//	   nilerr     — находит возврат nil при ненулевой ошибке (и наоборот).
//
//	5. Собственный анализатор:
//	   noosexitinmain — запрещает прямой вызов os.Exit в main.main (пакет main).
package main
